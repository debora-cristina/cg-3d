#pragma once
// classes example
#include <GL/glut.h>


class Pontos {
	float x, y, z;


public:

	Pontos() {

	}

	void setX(float x) {
		this->x = x;
	}

	float getX() {
		return x;
	}


	void setY(float y) {
		this->y = y;
	}

	float getY() {
		return y;
	}


	void setZ(float z) {
		this->z = z;
	}

	float getZ() {
		return z;
	}


};
#pragma once
